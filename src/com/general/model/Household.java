package com.general.model;
//家庭户籍
public class Household {
	private String householder_id;
	private String householder_no;
	private String personal_id;
	public String getHouseholder_id() {
		return householder_id;
	}
	public void setHouseholder_id(String householder_id) {
		this.householder_id = householder_id;
	}
	public String getHouseholder_no() {
		return householder_no;
	}
	public void setHouseholder_no(String householder_no) {
		this.householder_no = householder_no;
	}
	public String getPersonal_id() {
		return personal_id;
	}
	public void setPersonal_id(String personal_id) {
		this.personal_id = personal_id;
	}
}
