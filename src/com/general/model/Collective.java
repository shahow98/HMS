package com.general.model;

public class Collective {//集体户籍
	private String collective_id;
	private String collective_no;
	private String collective_name;
	public String getCollective_id() {
		return collective_id;
	}
	public void setCollective_id(String collective_id) {
		this.collective_id = collective_id;
	}
	public String getCollective_no() {
		return collective_no;
	}
	public void setCollective_no(String collective_no) {
		this.collective_no = collective_no;
	}
	public String getCollective_name() {
		return collective_name;
	}
	public void setCollective_name(String collective_name) {
		this.collective_name = collective_name;
	}
}
